const Mongoose = require("mongoose");

const Employee = new Mongoose.Schema({
  username: {
    type: String,
    required: true,
    unique: true,
  },
  name: {
    type: String,
    required: true,
  },
  password: {
    type: String,
    required: true,
  },
});

module.exports = Mongoose.model("Employee", Employee);